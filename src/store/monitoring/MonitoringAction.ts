import { SNACKBAR_OPEN } from 'store/actions'
import { axiosRequest } from 'store/axios'

export const listMonitoring = (payload) => ({
    type: 'LIST_MONITORING',
    payload,
})

const snackbarOpen = (message, type) => {
    return {
        type: SNACKBAR_OPEN,
        open: true,
        message: message,
        anchorOrigin: { vertical: 'top', horizontal: 'right' },
        variant: 'alert',
        alertSeverity: type,
    }
}

// async request
export const getMonitoringRequest = () => {
    return async (dispatch) => {
        try {
            const { data } = await axiosRequest('post', 'monitor/get/', {
                _all_: true,
            })
            dispatch(listMonitoring(data.data))
            dispatch(snackbarOpen('Operaci√≥n exitosa', 'success'))
            return true
        } catch (error) {
            dispatch(snackbarOpen(error, 'error'))
        }
    }
}
